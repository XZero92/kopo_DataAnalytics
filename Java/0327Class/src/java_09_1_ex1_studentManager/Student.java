package java_09_1_ex1_studentManager;

public class Student {
    String key;
    String name;
    int age;
    String major;

    String getKey() {
        return this.key;
    }
    void setKey(String key) {
        this.key = key;
    }

    String getName() {
        return this.name;
    }
    void setName(String name) {
        this.name = name;
    }

    int getAge() {
        return this.age;
    }
    void setAge(int age) {
        this.age = age;
    }

    String getMajor() {
        return this.major;
    }
    void setMajor(String major) {
        this.major = major;
    }
}
